use crate::types::*;
use anyhow::{bail, Result};
use convert_case::{Case, Casing};
use std::{fmt::Write, path::PathBuf};

pub struct IdlGenerator {
    path: PathBuf,
    w: String,
}

impl IdlGenerator {
    pub fn new(path: PathBuf) -> Self {
        Self {
            path,
            w: String::new(),
        }
    }

    pub fn generate(mut self, idl: Idl) -> Result<String> {
        self.write_header()?;

        for t in idl.types {
            self.write_base_type(&t)?;
        }

        self.write_services(&idl.services)?;

        Ok(self.result_string())
    }

    fn result_string(self) -> String {
        self.w
    }

    fn write_header(&mut self) -> Result<()> {
        writeln!(
            self.w,
            "// Code generated by sails-client-gen from {}. DO NOT EDIT.",
            self.path.file_name().unwrap().to_string_lossy()
        )?;
        writeln!(self.w, "#![automatically_derived]")?;
        writeln!(self.w, "#![allow(unused)]")?;

        writeln!(self.w, "use parity_scale_codec::{{Encode, Decode}};")?;
        writeln!(self.w, "use sails_client::{{Sender, Call}};")?;

        Ok(())
    }

    fn write_base_type(&mut self, t: &BaseType) -> Result<()> {
        let formatted = self.format_inner_type(&t.def)?;

        let type_keyword = match t.def {
            InnerType::TypeName { .. } => "type",
            InnerType::Option { .. } => "type",
            InnerType::Result { .. } => "type",
            InnerType::Vec { .. } => "type",
            InnerType::Tuple { .. } => "struct",
            InnerType::Struct { .. } => "struct",
            InnerType::Enum { .. } => "enum",
        };

        let eq = match t.def {
            InnerType::TypeName { .. }
            | InnerType::Option { .. }
            | InnerType::Result { .. }
            | InnerType::Vec { .. } => "=",

            InnerType::Tuple { .. } | InnerType::Struct { .. } | InnerType::Enum { .. } => "",
        };

        let semi = match t.def {
            InnerType::Struct { .. } | InnerType::Enum { .. } => "",
            _ => ";",
        };

        if matches!(
            t.def,
            InnerType::Struct { .. } | InnerType::Tuple { .. } | InnerType::Enum { .. }
        ) {
            writeln!(self.w, "#[derive(PartialEq, Debug, Encode, Decode)]")?;
        }

        writeln!(
            self.w,
            "pub {} {} {} {} {}",
            type_keyword, t.tt.name, eq, formatted, semi,
        )?;
        Ok(())
    }

    fn write_services(&mut self, services: &[Service]) -> Result<()> {
        if services.len() != 1 {
            bail!(
                "only one service definition is supported right now, got: {}",
                services.len()
            );
        }

        let s = services.first().unwrap().clone();

        for m in &s.methods {
            writeln!(
                self.w,
                r#"
                #[doc(hidden)]
            "#
            )?;
            let args = m.args_struct();
            self.write_base_type(&args)?;
        }

        writeln!(
            self.w,
            r#"

    #[derive(Default, Clone)]
    pub struct Client {{
        program_id: [u8; 32],
    }}

    impl Client {{

    pub fn new() -> Self {{
        Self::default()
    }}

    pub fn with_program_id(mut self, program_id: impl Into<[u8; 32]>) -> Self {{
        self.program_id = program_id.into();
        self
    }}


    "#
        )?;

        for m in &s.methods {
            self.format_service_method(m)?;
        }

        writeln!(self.w, "}}")?;

        Ok(())
    }

    fn format_service_method(&mut self, m: &ServiceMethod) -> Result<()> {
        let args = m
            .def
            .args
            .iter()
            .map(|a| format!("{}: {}", a.name, self.format_inner_type(&a.tt).unwrap()))
            .collect::<Vec<_>>()
            .join(", ");

        let arg_names = m
            .def
            .args
            .iter()
            .map(|a| a.name.to_owned())
            .collect::<Vec<_>>()
            .join(", ");

        let output = self.format_inner_type(&m.def.output).unwrap();

        let name_camel = m.def.name.to_owned();
        let name_snake = m.def.name.to_case(Case::Snake);
        writeln!(
            self.w,
            r#"pub fn {name_snake}(&self, {args}) -> Call<{output}> {{
            let mut payload = Vec::from("{name_camel}/");
            {name_camel}RequestArgs {{ {arg_names} }}.encode_to(&mut payload);

            Call::new(payload)
                .with_program_id(self.program_id)
        }}"#
        )?;

        Ok(())
    }

    fn format_inner_type(&mut self, t: &InnerType) -> Result<String> {
        Ok(match t {
            InnerType::TypeName { def } => match def.name.as_str() {
                "string" => "String".to_string(),
                "null" => "()".to_string(),
                _ => def.name.clone(),
            },
            InnerType::Option { def } => format!("Option<{}>", self.format_inner_type(def)?),
            InnerType::Result { def } => format!(
                "Result<{}, {}>",
                self.format_inner_type(&def.ok)?,
                self.format_inner_type(&def.err)?
            ),
            InnerType::Vec { def } => format!("Vec<{}>", self.format_inner_type(def)?),
            InnerType::Tuple { def } => format!(
                "({})",
                def.fields
                    .iter()
                    .map(|f| self.format_inner_type(f))
                    .collect::<Result<Vec<_>>>()?
                    .join(", ")
            ),
            InnerType::Struct { def } => {
                let mut w = String::new();

                writeln!(w, "{{")?;

                for field in &def.fields {
                    if let InnerType::Enum { def } = &field.tt {
                        // enums can't be nested, so write to top level buffer and replace definition with SimpleTypeDecl
                        let name = format!("{}Variant", field.name.to_case(Case::Pascal));

                        let t = BaseType {
                            tt: SimpleTypeDecl { name: name.clone() },
                            def: InnerType::Enum { def: def.clone() },
                        };

                        self.write_base_type(&t)?;

                        writeln!(w, "{}: {},", field.name, name)?;
                        continue;
                    }

                    writeln!(w, "{}: {},", field.name, self.format_inner_type(&field.tt)?)?;
                }

                write!(w, "}}")?;

                w
            }
            InnerType::Enum { def } => {
                let mut w = String::new();

                writeln!(w, "{{")?;

                for variant in &def.variants {
                    match &variant.tt {
                        Some(InnerType::Enum { def }) => {
                            // enums can't be nested, so write to top level buffer and replace definition with SimpleTypeDecl
                            let name = format!("{}Variant", variant.name.to_case(Case::Pascal));

                            let t = BaseType {
                                tt: SimpleTypeDecl { name: name.clone() },
                                def: InnerType::Enum { def: def.clone() },
                            };

                            self.write_base_type(&t)?;

                            writeln!(w, "{}({}),", variant.name, name)?
                        }
                        Some(InnerType::Struct { def }) => writeln!(
                            w,
                            "{}{},",
                            variant.name,
                            self.format_inner_type(&InnerType::Struct { def: def.clone() })?
                        )?,
                        Some(tt) => {
                            writeln!(w, "{}({}),", variant.name, self.format_inner_type(tt)?)?
                        }
                        None => writeln!(w, "{},", variant.name)?,
                    }
                }

                write!(w, "}}")?;

                w
            }
        })
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn nested_enum() {
        let idl = r#"
        {
            "def": {
                "variants": [
                {
                    "name": "One"
                },
                {
                    "name": "Two",
                    "type": {
                    "def": {
                        "variants": [
                        {
                            "name": "Three"
                        },
                        {
                            "name": "Four"
                        }
                        ]
                    },
                    "kind": "variant"
                    }
                }
                ]
            },
            "kind": "variant",
            "type": {
                "name": "ThisThatAppManyVariants",
                "kind": "simple"
            }
        }
        "#;

        let tt: BaseType = serde_json::from_str(idl).unwrap();

        let mut generator = IdlGenerator::new(PathBuf::from("test"));
        generator.write_base_type(&tt).unwrap();

        insta::assert_snapshot!(generator.result_string());
    }

    #[test]
    fn nested_enum_in_struct() {
        let idl = r#"
            {
            "def": {
                "fields": [
                {
                    "name": "p2",
                    "type": {
                    "def": {
                        "name": "string",
                        "kind": "simple"
                    },
                    "kind": "typeName"
                    }
                },
                {
                    "name": "p3",
                    "type": {
                    "def": {
                        "variants": [
                        {
                            "name": "One"
                        },
                        {
                            "name": "Two"
                        }
                        ]
                    },
                    "kind": "variant"
                    }
                }
                ]
            },
            "kind": "struct",
            "type": {
                "name": "ThisThatAppDoThatParam",
                "kind": "simple"
            }
            }
        "#;

        let tt: BaseType = serde_json::from_str(idl).unwrap();

        let mut generator = IdlGenerator::new(PathBuf::from("test"));
        generator.write_base_type(&tt).unwrap();

        insta::assert_snapshot!(generator.result_string());
    }
}
